import sys
sys.path.insert(0, '.')

from libs.py.aec_shared.otel import get_current_trace_id, get_current_span_id
from opentelemetry import trace
from opentelemetry.sdk.trace import TracerProvider
from opentelemetry.sdk.trace.export import SimpleSpanProcessor, ConsoleSpanExporter

def test_trace_id_extraction():
    """Test that trace IDs can be extracted from the current context"""
    
    # Set up a basic tracer provider for testing
    trace.set_tracer_provider(TracerProvider())
    tracer = trace.get_tracer_provider().get_tracer("test_tracer")
    
    # Test with an active span
    with tracer.start_as_current_span("test_span") as span:
        trace_id = get_current_trace_id()
        span_id = get_current_span_id()
        
        print(f"Trace ID: {trace_id}")
        print(f"Span ID: {span_id}")
        
        # Validate that trace and span IDs are properly formatted
        assert trace_id is not None, "Trace ID should not be None"
        assert span_id is not None, "Span ID should not be None"
        print(f"Actual trace ID length: {len(trace_id)}")
        print(f"Actual span ID length: {len(span_id)}")
        # Note: Trace ID might be shorter due to leading zeros being trimmed
        assert len(trace_id) <= 32, "Trace ID should be at most 32 characters"
        assert len(span_id) <= 16, "Span ID should be at most 16 characters"
        assert all(c in '0123456789abcdef' for c in trace_id), "Trace ID should be hexadecimal"
        assert all(c in '0123456789abcdef' for c in span_id), "Span ID should be hexadecimal"
        
        print("✓ Trace ID extraction test passed")
    
    # Test without active span
    trace_id_no_span = get_current_trace_id()
    span_id_no_span = get_current_span_id()
    
    assert trace_id_no_span is None, "Trace ID should be None without active span"
    assert span_id_no_span is None, "Span ID should be None without active span"
    
    print("✓ No active span test passed")

def test_error_envelope_includes_trace_id():
    """Test that error responses include trace IDs"""
    
    # Mock error response that would be generated by the error handler
    error_response_with_trace = {
        "traceId": "1234567890abcdef1234567890abcdef",
        "code": "INTERNAL_ERROR",
        "message": "Something went wrong",
        "details": {}
    }
    
    error_response_without_trace = {
        "code": "INTERNAL_ERROR", 
        "message": "Something went wrong",
        "details": {}
    }
    
    # Validate that traceId is included when available
    assert "traceId" in error_response_with_trace, "Error response should include traceId"
    assert error_response_with_trace["traceId"] is not None, "traceId should not be None"
    print(f"Mock trace ID length: {len(error_response_with_trace['traceId'])}")
    assert len(error_response_with_trace["traceId"]) <= 32, "traceId should be at most 32 characters"
    
    # Validate structure without traceId (for cases where tracing is disabled)
    assert "code" in error_response_without_trace, "Error response should include code"
    assert "message" in error_response_without_trace, "Error response should include message"
    assert "details" in error_response_without_trace, "Error response should include details"
    
    print("✓ Error envelope trace ID test passed")

def test_trace_id_propagation():
    """Test that trace IDs are properly propagated across services"""
    
    # This would test HTTP header propagation in a real environment
